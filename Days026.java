package minggu2;

public class Days026 {

    public static void main(String[] args) {

        int X = 4, v = 3,
                n = 2, W = 5, S = 7;
        X += 4;
        v -= 3;
        n *= 2;
        W /= 5;
        S %= 2;
        

        System.out.println("hasil x +=: " + X);
        System.out.println("hasil v -=: " + v);
        System.out.println("hasil n *=: " + n);
        System.out.println("hasil w /=: " + W);
        System.out.println("hasil S%=: " + S);
    }

}
