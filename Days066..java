package minggu5;

import java.util.Scanner;

public class Days066 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        
        System.out.println("Masukkan nilai n: ");
        int n = s.nextInt();
        
        for (int i = 1; i <= n; i++) {
            System.out.println("*");
            
        }
        System.out.println();
        
    }

}
