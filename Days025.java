package minggu2;

public class Days025 {
    public static void main(String[] args) {
        
        int a = 7; 
        a++;
        a--;
        
        System.out.println(a++);
        System.out.println(a--);
        
        System.out.println(++a);
        System.out.println(--a);
       
        System.out.println(a);
        
    }

}
